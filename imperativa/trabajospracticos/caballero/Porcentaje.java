package imperativa.trabajospracticos.caballero;

public class Porcentaje {
    public static void main(String[] args) {
        int numero1 = 50;
        float numero2 = 17.5f;
        float porcentaje = numero1 * numero2/100;
        System.out.println("El " + numero2 + " por ciento de " + numero1 + " es igual a " + porcentaje);
    }
}
