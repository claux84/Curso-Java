package imperativa.trabajospracticos.padawan;

public class ConversionKilogramos {
    public static void main(String[] args) {
        float libras = 4;
        final float CONSTANTE = 0.4535f;
        float kilos = libras * CONSTANTE;
        System.out.println(libras + " libras son " + kilos + " Kilogramos");


    }
}
